    <p>
        Budismul este o religie și o filozofie orientală. Ea își are originea în India în secolul al VI-lea
                    î.e.n. și s-a răspândit într-o mare parte a Asiei Centrale și de Sud-Est. Se bazează pe învățăturile
                    lui Gautama Siddhartha (Buddha Shakyamuni), un gânditor indian care se crede că ar fi trăit între
                    560 î.e.n și 480 î.e.n.. De-a lungul timpului, budismul a suferit numeroase schimbări, în prezent
                    fiind o religie foarte divizată, fără o limbă sacră comună și fără o dogmă strictă, clar formulată.
                    Răspândit în China, Japonia, Sri Lanka, Coreea, Mongolia, Uniunea Myanmar, Thailanda, Vietnam și
                    Malaezia. Budismul aparține grupului de religii dharmice alături de hinduism și de jainism, păstrând
                    o puternică influență a elementelor constituente ale acestor două religii. Mai este numit și „Buddha
                    Dharma”, ceea ce înseamnă în limbile sanscrită și pali (limbile textelor antice budiste)
                    „învățăturile Celui Luminat”.</p>
                <p> În prezent, istoricii fixează această dată în 560 î.Hr. Veridicitatea istorică a întemeietorului
                    budismului, Gautama Buddha (623-543 î.Hr., după tradiția budistă din Birmania și Thailanda; 560-480
                    î.Hr., după istoricii moderni), este confirmată de majoritatea cercetătorilor din zilele noastre în
                    pofida vechilor teorii din trecut care îl considerau pe acesta mai degrabă un personaj mitologic. Cu
                    toate acestea, biografia lui Gautama Buddha, așa cum este ea prezentată de Asvaghosha în
                    Buddhacarita (cea mai veche relatare existentă a vieții profetului) abundă de elemente fantastice și
                    legendare. Budiștii consideră că prințul Gautama Siddhartha s-a născut în Lumbini și a fost crescut
                    în Kapilavastu, aproape de actuala frontieră dintre India și Nepal. Potrivit tradiției, tatăl lui
                    Buddha era conducătorul unei formațiuni tribale numite Sakya.</p>
